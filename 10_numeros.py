print("For: ")

print("Numeros pares: ")
for n in range(0, 19, 2):
    print(n)

print("\n")

print("Numeros impares: ")
for o in range(1, 20, 2):
    print(o)

print("\n")

print("While: ")

print("Numeros pares: ")
x = 0
while x < 19:
    print(x)
    x += 2

print("\n")

print("Numeros impares: ")
y = 1
while y < 20:
    print(y)
    y += 2